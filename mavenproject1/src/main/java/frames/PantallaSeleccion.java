    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        String tarjeta = txtFldTarjeta.getText(); 
    
    if (control.procesarTransaccion(tarjeta)) {
        PantallaTicket ticket = new PantallaTicket(ventaActual);
        ticket.setVisible(true);
        this.dispose();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error en la autorización del pago. Tienen que ser maximo 16 caracteres en la tarjeta.");
    }
    }//GEN-LAST:event_btnPagarActionPerformed

