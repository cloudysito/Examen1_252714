    @Override
    public void actualizar() {
    pnlListaProductos.removeAll();

    for (DetalleVenta d : ventaActual.getDetalles()) {
        
        JPanel fila = new JPanel(new java.awt.BorderLayout());
        fila.setBackground(java.awt.Color.WHITE);
        fila.setMaximumSize(new Dimension(Integer.MAX_VALUE, 40)); 

        JLabel lblNombre = new JLabel(d.getProducto().getNombre() + " (x" + d.getCantidad() + ")");
        lblNombre.setFont(new java.awt.Font("Segoe UI", 1, 13));
        fila.add(lblNombre, java.awt.BorderLayout.WEST);

        JButton btnEliminar = new JButton();
        btnEliminar.setText("X");      
        
        btnEliminar.setBorderPainted(false);
        btnEliminar.setContentAreaFilled(false);
        btnEliminar.setFocusPainted(false);
        btnEliminar.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

        btnEliminar.addActionListener(e -> {
            control.eliminarSeleccion(d); 
        });

        fila.add(btnEliminar, java.awt.BorderLayout.EAST);

        pnlListaProductos.add(fila);
        pnlListaProductos.add(javax.swing.Box.createVerticalStrut(12)); 
    }
    
    lblSubtotal1.setText("$" + String.format("%.2f", ventaActual.getSubtotal()));
    lblImpuesto1.setText("$" + String.format("%.2f", ventaActual.getImpuesto()));
    lblTotal1.setText("$" + String.format("%.2f", ventaActual.getTotal()));
    
    pnlListaProductos.revalidate();
    pnlListaProductos.repaint();
}
    
